def calculate_co_aqi(co_concentration: float) -> int:
    """
    Calculates the EPA AQI strictly based on the 8-hour Carbon Monoxide (CO) concentration (ppm or mg/m3).
    Formula: I = ((I_high - I_low) / (C_high - C_low)) * (C - C_low) + I_low
    """
    # Breakpoints for CO: (C_low, C_high, I_low, I_high)
    breakpoints = [
        (0.0, 4.4, 0, 50),        # Good
        (4.5, 9.4, 51, 100),      # Moderate
        (9.5, 12.4, 101, 150),    # Unhealthy for Sensitive Groups
        (12.5, 15.4, 151, 200),   # Unhealthy
        (15.5, 30.4, 201, 300),   # Very Unhealthy
        (30.5, 40.4, 301, 400),   # Hazardous
        (40.5, 50.4, 401, 500)    # Hazardous
    ]
    
    # Cap negative numbers generated by the model to 0
    c = max(0.0, round(float(co_concentration), 1))
    
    for (c_low, c_high, i_low, i_high) in breakpoints:
        if c_low <= c <= c_high:
            # Interpolation calculation
            aqi = ((i_high - i_low) / (c_high - c_low)) * (c - c_low) + i_low
            return int(round(aqi))
            
    # If greater than 50.4, we cap or extrapolate
    if c > 50.4:
        return 500
        
    return 0
